            Object[][] datosMantenimiento = con.consultaMantenimientoPorFactura();
            Object datosMant[] = new String[8];//columnas

            System.arraycopy(datosMantenimiento[0], 0, datosMant, 0, 8);
            
            num_factura.setText(String.valueOf(datosMant[0]));
