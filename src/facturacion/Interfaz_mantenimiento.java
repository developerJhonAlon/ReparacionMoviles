            combo_formapago.setSelectedItem(datosMant[4]);
         
            txtObservacion.setText(String.valueOf(datosMant[5]));
            precio.setText(String.valueOf(datosMant[6]));
            cmbEstado.setSelectedItem(datosMant[7]);

